def  suma ( a , b ): # Se crea una función suma que recibira 2 parametros
    return  a + b  # Se retornan los dos parametros de la funcion

def  suma ( a , b , c ): # Se crea una función suma que recibira tres parametros
    return  a + b + c  # Se retornan los tres parametros de la funcion

print ( suma ( 1 , 2 , 3 )) #Se imprime la funcion con los parametro 1,2,3